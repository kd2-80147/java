package com.sunbeam;

public class CarCost_Ques_3 {
	int miles;
	int gasPrice;
	int mileage;
	int parkingFees;
	int toll;

	public CarCost_Ques_3() {

	}

	public CarCost_Ques_3(int miles, int gasPrice, int mileage, int parkingFees, int toll) {
		super();
		this.miles = miles;
		this.gasPrice = gasPrice;
		this.mileage = mileage;
		this.parkingFees = parkingFees;
		this.toll = toll;
	}

	/**
	 * @return the miles
	 */
	public int getMiles() {
		return miles;
	}

	/**
	 * @return the gasPrice
	 */
	public int getGasPrice() {
		return gasPrice;
	}

	/**
	 * @return the mileage
	 */
	public int getMileage() {
		return mileage;
	}

	/**
	 * @return the parkingFees
	 */
	public int getParkingFees() {
		return parkingFees;
	}

	/**
	 * @return the toll
	 */
	public int getToll() {
		return toll;
	}
	
	
}
